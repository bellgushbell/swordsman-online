            // ฟังก์ชันดึงข้อมูลข่าวจาก PHP
            document.addEventListener('DOMContentLoaded', function() {
                axios.get('../../database/player/contents_index.php') // URL ของไฟล์ PHP ที่ส่งข้อมูล JSON
                    .then(function(response) {
                        const newsData = response.data; // ข้อมูลที่ได้รับมาในรูปแบบ JSON
                        console.log('data', newsData); // แสดงข้อมูลใน console เพื่อตรวจสอบ

                        // เรียงลำดับจากใหม่ -> เก่า
                        newsData.sort((a, b) => new Date(b.created_at) - new Date(a.created_at));

                        let currentPage = 1;
                        const itemsPerPage = 12;
                        let currentCategory = localStorage.getItem("activeTab") || "All"; // เก็บหมวดหมู่ที่เลือกใน localStorage

                        // ฟังก์ชันในการกรองข่าวตามหมวดหมู่
                        function filterNewsByCategory() {
                            return currentCategory === "All" ?
                                newsData :
                                newsData.filter(news => {
                                    if (currentCategory === "Promotions" && news.category_name === "Promotions") {
                                        return true;
                                    } else if (currentCategory === "Events" && news.category_name === "Events") {
                                        return true;
                                    } else if (currentCategory === "News" && news.category_name === "News") {
                                        return true;
                                    }
                                    return false; // กรองข้อมูลที่ไม่ตรงกับประเภทที่เลือก
                                });
                        }

                        // ฟังก์ชันแสดงข่าว
                        function renderNews() {
                            const newsContainer = document.getElementById("news-container");
                            newsContainer.innerHTML = "";

                            let filteredNews = filterNewsByCategory(); // กรองข่าวตามหมวดหมู่ที่เลือก

                            // หาช่วงของข่าวที่จะโชว์ในหน้า (เริ่มต้นแสดง 12 รายการแรก)
                            const startIndex = (currentPage - 1) * itemsPerPage;
                            const paginatedNews = filteredNews.slice(startIndex, startIndex + itemsPerPage);

                            // แสดงข่าวที่กรองและแบ่งหน้า
                            paginatedNews.forEach(news => {
                                const li = document.createElement("li");
                                const dateOnly = news.created_at.substring(0, 10); // 10 ตัวแรก (YYYY-MM-DD)
                                // เพิ่มลิงก์ที่แสดงรายละเอียดของข่าว

                                const newtypefilter = news.category_name === "News" ? "ประกาศ" : news.category_name;

                                // กำหนดสีตามประเภท
                                let newTypeColor;
                                if (news.category_name === "News") {
                                    newTypeColor = "rgb(127,169,209)";
                                } else if (news.category_name === "Events") {
                                    newTypeColor = "rgb(153, 127, 209)";
                                } else if (news.category_name === "Promotions") {
                                    newTypeColor = "rgb(209, 138, 127)";
                                } else {
                                    newTypeColor = "rgba(0, 0, 0, 0.8)"; // สีดำ (black)
                                }
                                li.innerHTML = `
                                                <div class="news-item" style="cursor: pointer;display: flex; justify-content: flex-start;align-items: flex-start;">
                                                        <div class="news-header" style="display: flex; justify-content: center; align-items: center;">
                                                            <div class="news-item-div-img" style="display: flex; justify-content: center; align-items: center; width: 300px; height: 200px; overflow: hidden; border-radius: 6px !important;">
                                                                <img src="../../images/news/${news.image}" alt="${news.header_thumbnail}" style="max-width: 100%; max-height: 100%; object-fit: contain;">
                                                            </div>
                                                        </div>

                                                    <div class="news-text-title" style="display: flex; width: 100%; margin-top:20px;">
                                                        <div style="display: flex; justify-content: space-between; width: 100%;position: relative;">
                                                            <div display: flex; justify-content: flex-start;  flex-direction: column;  >
                                                            &nbsp;
                                                        <span class="newsall-type-text" style="color: white; display: inline-block; padding: 2px 8px !important; border-radius: 5px;  
                                                        background: linear-gradient(135deg, ${newTypeColor} 0%, ${newTypeColor} 100%, #fff 100%); margin-bottom: 5px;">
                                                        ${newtypefilter}
                                                        </span>
                                                            &nbsp;
                                                            <a href="#" class="text-decoration-none" style="display: block; margin-bottom: 5px; margin-left: 10px;">${news.header_thumbnail}</a>
                                                            <span  class="text-decoration-none" style="display: block; margin-bottom: 5px; margin-left: 10px; font-size: 12px;">${news.sub_header_thumbnail}...</span>
                                                            </div>
                                                        
                                                        
                                                    
                                                        <div style="position: absolute; top: 0; right: 5px;">
                                                        <span class="date">${dateOnly}</span>
                                                    </div>
                                                        
                                                        </div>

                                                    </div>
                                                </div>
                                                `;

                                // ฟังก์ชันเมื่อคลิกที่ <li> จะไปที่หน้า news detail
                                li.addEventListener("click", function() {
                                    window.location.href = `../../database/player/contents_read_detail.php?id=${news.id}`; // 
                                });

                                newsContainer.appendChild(li);
                            });

                            // แสดงหมายเลขหน้า
                            document.getElementById("pageNumber").textContent = currentPage;

                            // ปิดปุ่ม "ก่อนหน้า" ถ้าอยู่หน้าแรก
                            document.getElementById("prevPage").disabled = currentPage === 1;

                            // ปิดปุ่ม "ถัดไป" ถ้าเป็นหน้าสุดท้าย
                            const totalPages = Math.ceil(filteredNews.length / itemsPerPage);
                            document.getElementById("nextPage").disabled = currentPage === totalPages;
                        }

                        // เรียกใช้ renderNews() เมื่อเข้าหน้าแรก
                        renderNews();

                        // เปลี่ยนแท็บเมื่อผู้ใช้คลิก
                        document.querySelectorAll(".news-tabs button").forEach(button => {
                            button.addEventListener("click", () => {
                                // เปลี่ยนสถานะของปุ่ม
                                document.querySelectorAll(".news-tabs button").forEach(btn => btn.classList.remove("active"));
                                button.classList.add("active");

                                // บันทึกค่าหมวดหมู่ใน localStorage
                                currentCategory = button.getAttribute("data-category");
                                currentPage = 1; // รีเซ็ตหน้าเป็นหน้าแรก

                                localStorage.setItem("activeTab", currentCategory); // เก็บสถานะหมวดหมู่ใน localStorage

                                renderNews(); // แสดงข่าว
                            });
                        });

                        // Pagination - คลิก "ก่อนหน้า"
                        document.getElementById("prevPage").addEventListener("click", () => {
                            if (currentPage > 1) {
                                currentPage--;
                                renderNews();
                            }
                        });

                        // Pagination - คลิก "ถัดไป"
                        document.getElementById("nextPage").addEventListener("click", () => {
                            const filteredNews = filterNewsByCategory(); // ใช้ฟังก์ชันกรองข่าว
                            const totalPages = Math.ceil(filteredNews.length / itemsPerPage);

                            if (currentPage < totalPages) {
                                currentPage++;
                                renderNews();
                            }
                        });
                    })
                    .catch(function(error) {
                        console.error('Error fetching data:', error);
                    });
            });


            $(document).ready(function() {
                const tabButtons = $(".news-tabs button");
                const tabPanes = $(".tab-pane");
                const newsContainer = $("#news-container");

                function activateTab(tabId) {
                    tabPanes.hide(); // ซ่อนทุกแท็บ
                    $(`#${tabId}-tab-pane`).show(); // แสดงแท็บที่ถูกเลือก
                    tabButtons.removeClass("active");
                    $(`[data-category="${tabId}"]`).addClass("active");
                }

                // ดึงค่า tab จาก URL parameter
                const urlParams = new URLSearchParams(window.location.search);
                let activeTab = urlParams.get("tab") || "All"; // ค่าเริ่มต้นเป็น "all"

                // ถ้าไม่มีค่าใน URL ใช้ค่าใน localStorage แทน
                if (!urlParams.has("tab")) {
                    activeTab = localStorage.getItem("activeTab") || "All";
                }

                activateTab(activeTab);

                tabButtons.on("click", function() {
                    const targetTab = $(this).data("category");
                    activateTab(targetTab);
                    localStorage.setItem("activeTab", targetTab); // บันทึกค่าของแท็บที่เลือกใน localStorage

                    // อัพเดต URL ให้มีการเลือกแท็บเพื่อคงสถานะ
                    const currentUrl = new URL(window.location);
                    currentUrl.searchParams.set("tab", targetTab);
                    history.pushState(null, "", currentUrl.toString());
                });
            });
